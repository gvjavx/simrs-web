package com.neurix.simrs.bpjs.aplicare.model;

public class KetersediaanKamarRequest {
    private String kodeKelas;
    private String kodeRuang;
    private String namaRuang;
    private String kapasitas;
    private String tersedia;
    private String tersediaPria;
    private String tersediaWanita;
    private String tersediaPriaWanita;

    public String getKodeKelas() {
        return kodeKelas;
    }

    public void setKodeKelas(String kodeKelas) {
        this.kodeKelas = kodeKelas;
    }

    public String getKodeRuang() {
        return kodeRuang;
    }

    public void setKodeRuang(String kodeRuang) {
        this.kodeRuang = kodeRuang;
    }

    public String getNamaRuang() {
        return namaRuang;
    }

    public void setNamaRuang(String namaRuang) {
        this.namaRuang = namaRuang;
    }

    public String getKapasitas() {
        return kapasitas;
    }

    public void setKapasitas(String kapasitas) {
        this.kapasitas = kapasitas;
    }

    public String getTersedia() {
        return tersedia;
    }

    public void setTersedia(String tersedia) {
        this.tersedia = tersedia;
    }

    public String getTersediaPria() {
        return tersediaPria;
    }

    public void setTersediaPria(String tersediaPria) {
        this.tersediaPria = tersediaPria;
    }

    public String getTersediaWanita() {
        return tersediaWanita;
    }

    public void setTersediaWanita(String tersediaWanita) {
        this.tersediaWanita = tersediaWanita;
    }

    public String getTersediaPriaWanita() {
        return tersediaPriaWanita;
    }

    public void setTersediaPriaWanita(String tersediaPriaWanita) {
        this.tersediaPriaWanita = tersediaPriaWanita;
    }
}
